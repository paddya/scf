package scf.parser.exception;



/**
 *
 * @author paddya
 */
public class ParserIllegalColumnException extends ParserException
{



    public ParserIllegalColumnException()
    {
    }



    public ParserIllegalColumnException(String message)
    {
        super(message);
    }



    public ParserIllegalColumnException(String message, Throwable cause)
    {
        super(message, cause);
    }



    public ParserIllegalColumnException(Throwable cause)
    {
        super(cause);
    }



    public ParserIllegalColumnException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace)
    {
        super(message, cause, enableSuppression, writableStackTrace);
    }
    
    
    
}


